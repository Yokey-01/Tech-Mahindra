public class Task3 {
    public static void main(String[] args) {
        int a = 7;
        int b = 8;

        System.out.println("a >> 1: " + (a >> 1));
        System.out.println("a ^ b: " + (a ^ b));
        System.out.println("~a: " + (~a));
        System.out.println("a & b: " + (a & b));
        System.out.println("a << 1: " + (a << 1));
        System.out.println("a | b: " + (a | b));


        boolean y = true;
        boolean x = false;

        System.out.println("x || y: " + (x || y));
        System.out.println("x && y: " + (x && y));
        System.out.println("!x: " + (!x));
    }
}